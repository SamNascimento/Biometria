package viewmodel;

public class AprovarAgrotoxicoViewModel {
	
	private String idAgrotoxico;
	
	private String nomeProprietario;
	
	private String nomeAgrotoxico;
	
	private String status;

	public String getIdAgrotoxico() {
		return idAgrotoxico;
	}

	public void setIdAgrotoxico(String idAgrotoxico) {
		this.idAgrotoxico = idAgrotoxico;
	}

	public String getNomeProprietario() {
		return nomeProprietario;
	}

	public void setNomeProprietario(String nomeProprietario) {
		this.nomeProprietario = nomeProprietario;
	}

	public String getNomeAgrotoxico() {
		return nomeAgrotoxico;
	}

	public void setNomeAgrotoxico(String nomeAgrotoxico) {
		this.nomeAgrotoxico = nomeAgrotoxico;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
}
